package Loop_study;

public class prog1 {

	public static void main(String[] args) {
	System.out.println("*");
	System.out.println("**");
	System.out.println("***");
	System.out.println("****");
	System.out.println("*****");
	
	System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@");
	
	//print* from 1+2+3+4
	for(int i=1; i<=1; i+=1)
	{
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("****");
		System.out.println("*****");
		
	}
	
	
	}

}
