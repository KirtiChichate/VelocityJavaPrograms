package Loop_study;

public class DoWhile_mix {

	public static void main(String[] args) {
		//print 1st char alpha from a to z
		//print table of 5--150//
		
		char alpha='A';
		int a=5;
		
		do
		{
			System.out.println(alpha);
			alpha+=1;
			System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
			System.out.println(a);
			a+=5;
			
		}
        while(alpha<='Z');
        	
        	while(a<=150);
        		
        	
	}

}
