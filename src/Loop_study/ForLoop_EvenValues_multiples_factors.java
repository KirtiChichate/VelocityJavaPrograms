package Loop_study;

public class ForLoop_EvenValues_multiples_factors {

	public static void main(String[] args) {

//find the even values between 2---100
		for(int k=2; k<=100; k+=2)
		{
			System.out.println(k);
		}
System.out.println("*****************************************************************");


	//find the multiple of 2 from 1---100	
	for(int j=1; j<=100; j*=2)
	{
		System.out.println(j);
	}
		System.out.println("***************************************************************");
		
		
	
		
	}
	

}
