package Loop_study;

public class While_loopstudy {

	public static void main(String[] args) {
		
		int x=1;
		while(x<=50)
		{
			System.out.println(x);
			x++;
			
		}
		System.out.println("#########################################");
		//multiples of 5 from 5---100
		int a=10;
		while(a<=100)
		{
			System.out.println(a);
			a*=5;
		}
		
		
		
		
	}

}
