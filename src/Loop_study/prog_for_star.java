package Loop_study;

public class prog_for_star {

	public static void main(String[] args) {
		// this is example undr nested for loop
				
				//print* from 1+2+3+4
				for (int i=1; i<=5; i++)

				{
					for(int j=1; j<=i; j++)
					{
					System.out.print("*");
					}
					System.out.println();
				}	
				
				
				
	}

}
