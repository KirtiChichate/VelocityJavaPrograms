package inheritance_multiLevel;

public class Animal
{
	String colour="white";  //global nonstatic nonprimitive variable//
	static int height=20; // global static primitive variable//
	
	public void eat()
	{
		System.out.println("Animal is eating");
		
	}
	
	public static void run()
	{
		System.out.println("Animal is running");
	}

}
